// ------------------------------------------------------------
//  Fonction principale : applique une langue à l’UI
// ------------------------------------------------------------
export function applyTranslation(lang) {
    const t = translations[lang];
    if (!t) return;

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;

        if (!key || !t[key]) return;

        if (el.tagName === "OPTGROUP") {
            el.label = t[key];
        }
        else if (el.tagName === "OPTION" && el.dataset.icon) {
            el.textContent = `${el.dataset.icon} ${t[key]}`;
        }
        else {
            el.innerHTML = t[key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if (key && t[key]) el.placeholder = t[key];
    });

    // 🔥 Reconstruction propre du layout
    const layoutEl = document.getElementById("layoutIndicator");
    if (layoutEl && window.currentLayoutCode) {
        const map = {
            "BE": "AZERTY (BE)",
            "FR": "AZERTY (FR)",
            "US": "QWERTY (US)"
        };
        const layoutText = map[window.currentLayoutCode] || window.currentLayoutCode;
        layoutEl.textContent = t.layout_label + layoutText;
    }

    // Correction des labels GPIO
    document.querySelectorAll(".gpio-row label").forEach(label => {
        if (label.textContent.match(/Touche|Key/)) {
            const num = label.textContent.replace(/\D/g, "");
            label.textContent = `${t.system_gpio_key} ${num}`;
        }
        if (label.textContent.includes("Ruban") || label.textContent.includes("LED strip")) {
            label.textContent = t.system_gpio_led;
        }
    });
}

export function setLanguage(lang) {
    window.currentLang = lang;
    applyTranslation(lang);
}

window.currentLang = "fr";
